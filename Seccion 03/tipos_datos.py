# Ejemplo de tipos de datos

# Entero
edad = 30
print(f'Edad: {edad}')

# Numero con punto flotante
altura = 1.75
print(f'Altura: {altura}')

# Cadena de texto
nombre = 'Jose'
print(f'Nombre: {nombre}')

# Tipo Boleano
es_estudiante = True
print(f'Estudiante: {es_estudiante}')

# None, ausencia de valor
direccion = None
print(f'Direccion: {direccion}')
