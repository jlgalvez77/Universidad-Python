# Operadores Aritmeticos

a = 10
b = 3

# Suma +
suma = a + b
print(f'Resultado de sumar: {suma}')

# Resta -
resta = a - b
print(f'Resultado de resta: {resta}')

# Multiplicación *
multiplicacion = a * b
print(f'Resultado de multiplicacion: {multiplicacion}')

# División
division = a / b
print(f'Resultado de division: {division}')

# División entera
division_entera = a // b
print(f'Resultado de division_entera: {division_entera}')

# Modulo %
modulo = a % b
print(f'Resultado de modulo: {modulo}')

# Exponente **
exponencial = a ** b
print(f'Resultado de exponencial: {exponencial}')
